b = "Hello World"
print(b[2:5])

"""
Slice from the start
"""

b = "Hello World"
print(b[:5])

"""
Slice to the end
"""

b = "Hello World"
print(b[2:])

"""
Navigate backwards
"""

b = "Hello World"
print("the character in -2 is", b[-2])
print("the character in -5 is", b[-5])
print("the characters from -5 to -2 are", b[-5:-2])